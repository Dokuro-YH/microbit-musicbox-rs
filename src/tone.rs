use defmt::Format;

macro_rules! tones {
    (
        $($key:ident: $freq:expr),+
    ) => {
        #[derive(Format, Debug, Clone, Copy, PartialEq, Eq)]
        pub enum Tone {
            $(
                $key,
            )*
        }

        impl Tone {
            pub fn freq(&self) -> f32 {
                match *self {
                    $(Tone::$key => $freq,)*
                }
            }
        }
    };
}

